<div class="legal-article" id="article-1">
    <h2><span class="art-num">01</span> Objet de la plateforme</h2>
    <p>MESOTRAVO est une plateforme numérique de mise en relation entre :</p>
    <ul>
        <li>des clients recherchant des services ou travaux à domicile,</li>
        <li>des artisans indépendants exerçant légalement une activité professionnelle.</li>
    </ul>
    <div class="legal-warning">
        La plateforme n'exécute aucun travail et n'emploie aucun artisan.<br>
        Le contrat de prestation est conclu directement entre le client et l'artisan, conformément au droit en vigueur en République du Bénin et au droit OHADA.
    </div>
</div>

<div class="legal-article" id="article-2">
    <h2><span class="art-num">02</span> Statut de la plateforme</h2>
    <p>La plateforme agit uniquement comme intermédiaire technique et commercial :</p>
    <ul>
        <li>organisation des échanges,</li>
        <li>suivi de la mission,</li>
        <li>sécurisation du paiement via un prestataire agréé.</li>
    </ul>
    <p>Elle n'est pas responsable de :</p>
    <ul>
        <li>la qualité technique des travaux,</li>
        <li>les dommages matériels liés à l'intervention,</li>
        <li>les relations personnelles entre les parties.</li>
    </ul>
    <p>Cette clarification est exigée pour toute plateforme e-commerce au Bénin.</p>
</div>

<div class="legal-article" id="article-3">
    <h2><span class="art-num">03</span> Création et utilisation des comptes</h2>
    <h3>Clients</h3>
    <ul>
        <li>Inscription gratuite.</li>
        <li>Les informations fournies doivent être exactes.</li>
        <li>Toute tentative de fraude, de non-paiement ou de contournement entraîne la suspension du compte.</li>
    </ul>
    <h3>Artisans</h3>
    <p>L'artisan déclare :</p>
    <ul>
        <li>être professionnel indépendant déclaré (personne physique ou morale),</li>
        <li>être autorisé à exercer son métier,</li>
        <li>disposer des assurances professionnelles nécessaires (si applicables),</li>
        <li>répondre personnellement de ses actes et de son travail.</li>
    </ul>
    <p>La plateforme peut vérifier les informations et refuser ou suspendre un compte sans justification extensive.</p>
</div>

<div class="legal-article" id="article-4">
    <h2><span class="art-num">04</span> Déroulement d'une mission</h2>
    <div class="legal-steps">
        <div class="legal-step"><div class="legal-step-num">1</div><div>Le client exprime un besoin</div></div>
        <div class="legal-step"><div class="legal-step-num">2</div><div>La plateforme propose la mission à un artisan disponible</div></div>
        <div class="legal-step"><div class="legal-step-num">3</div><div>L'artisan accepte ou refuse</div></div>
        <div class="legal-step"><div class="legal-step-num">4</div><div>Diagnostic sur place, le cas échéant</div></div>
        <div class="legal-step"><div class="legal-step-num">5</div><div>Devis soumis dans l'application</div></div>
        <div class="legal-step"><div class="legal-step-num">6</div><div>Acceptation par le client</div></div>
        <div class="legal-step"><div class="legal-step-num">7</div><div>Paiement exclusivement via la plateforme</div></div>
    </div>
    <div class="legal-warning">Tout paiement en dehors de la plateforme est interdit pour une mission issue de celle-ci.</div>
</div>

<div class="legal-article" id="article-5">
    <h2><span class="art-num">05</span> Devis, prix et paiement</h2>
    <ul>
        <li>Les prix sont librement fixés par l'artisan.</li>
        <li>Le client valide obligatoirement le devis avant travaux.</li>
        <li>Le paiement est effectué via un prestataire de paiement agréé BCEAO / UEMOA (Mobile Money, carte, etc.).</li>
        <li>La plateforme ne conserve pas les fonds, mais les fait transiter de manière sécurisée.</li>
    </ul>
</div>

<div class="legal-article" id="article-6">
    <h2><span class="art-num">06</span> Commission de la plateforme</h2>
    <p>La plateforme perçoit une commission pour son service.</p>
    <p>
        <strong>Règle simple et claire :</strong> toute mission obtenue grâce à la plateforme donne lieu au paiement de la commission,
        même si les parties tentent de finaliser en dehors de celle-ci.
        Cette obligation s'applique pour toute relation client-artisan initiée via la plateforme pendant une durée de [X mois].
    </p>
    <p>En cas de contournement :</p>
    <ul>
        <li>suspension ou suppression du compte,</li>
        <li>blocage des paiements,</li>
        <li>exclusion définitive.</li>
    </ul>
</div>

<div class="legal-article" id="article-7">
    <h2><span class="art-num">07</span> Obligations spécifiques des artisans</h2>
    <p>L'artisan s'engage à :</p>
    <ul>
        <li>respecter les délais annoncés,</li>
        <li>exécuter les travaux conformément au devis accepté,</li>
        <li>adopter un comportement respectueux et professionnel,</li>
        <li>respecter les règles de sécurité au domicile du client.</li>
    </ul>
    <p>La plateforme peut appliquer : avertissement, suspension, déréférencement.</p>
</div>

<div class="legal-article" id="article-8">
    <h2><span class="art-num">08</span> Obligations des clients</h2>
    <p>Le client s'engage à :</p>
    <ul>
        <li>fournir des informations exactes,</li>
        <li>permettre l'accès au lieu d'intervention,</li>
        <li>respecter l'artisan,</li>
        <li>procéder au paiement tel que validé.</li>
    </ul>
    <p>Toute tentative de fraude ou de pression sur l'artisan entraîne la suspension du compte.</p>
</div>

<div class="legal-article" id="article-9">
    <h2><span class="art-num">09</span> Avis, notation et réputation</h2>
    <ul>
        <li>Les avis doivent être honnêtes, factuels et respectueux.</li>
        <li>Les faux avis ou abus peuvent être supprimés.</li>
        <li>Le système de notation conditionne la visibilité des artisans.</li>
    </ul>
    <p>C'est un élément central de confiance sur la plateforme.</p>
</div>

<div class="legal-article" id="article-10">
    <h2><span class="art-num">10</span> Litiges et médiation</h2>
    <ul>
        <li>La plateforme peut intervenir comme facilitateur de dialogue.</li>
        <li>Elle peut analyser les éléments disponibles dans l'application.</li>
        <li>Elle peut décider d'un déblocage, blocage ou remboursement partiel selon les cas.</li>
    </ul>
    <p>Elle ne remplace pas les tribunaux, mais facilite une résolution amiable.</p>
</div>

<div class="legal-article" id="article-11">
    <h2><span class="art-num">11</span> Responsabilité</h2>
    <ul>
        <li>Chaque artisan est seul responsable de son travail.</li>
        <li>La plateforme n'est pas responsable des dommages matériels ou corporels.</li>
        <li>Les utilisateurs reconnaissent utiliser le service à leurs risques contractuels.</li>
    </ul>
    <p>Cette clause est conforme au cadre OHADA et e-commerce béninois.</p>
</div>

